    private void ConfirmarFielActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmarFielActionPerformed
        // TODO add your handling code here:
        CadastroDizimista dizimista = new CadastroDizimista();
        // int codigo, String nome, String nascimento, float cpf, String rua,int numero, String bairro, String cidade, String estado
        dizimista.setNome(NomeFiel.getText().toString());
        dizimista.setNascimento(NascimentoFiel.getText().toString());
        dizimista.setCpf(CpfFiel.getText().toString());
        dizimista.setRua(RuaFiel.getText().toString());
        dizimista.setNumero(Long.parseLong(NumFiel.getText().toString()));
        dizimista.setBairro(BairroFiel.getText().toString());
        dizimista.setCidade(CidadeFiel.getText().toString());
        dizimista.setEstado(EstadoFiel.getText().toString());
        dizimistaDAO.inserir(dizimista);

        JOptionPane.showMessageDialog(null, " Novo fiel cadastrado!!");


    }//GEN-LAST:event_ConfirmarFielActionPerformed

